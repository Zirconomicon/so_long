#include "ft_printf.h"

int	hexa_size(unsigned long long n)
{
	int	size;

	size = 1;
	/*if (n < 0)
		n = 4294967295 - n;*/
	while (n >= 16)
	{
		n = n / 16;
		size++;
	}
	return (size);
}
